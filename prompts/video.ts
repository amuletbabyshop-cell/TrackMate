// prompts/video.ts — 動画分析プロンプト（全種目対応）

import type { AthleticsEvent } from '../types'

// ── 種目 → コーチ役割・フォーカスポイント ───────────────────

function getContext(event: AthleticsEvent): { role: string; focus: string } {
  switch (event) {
    // ── 短距離 ────────────────────────────────────────────
    case '100m':
      return {
        role: '元日本代表の100m短距離コーチ',
        focus: `100mの選手です。スタートブロックの設定、一次加速（0〜30m）の姿勢角度、最高速局面（60〜80m）での体幹の立位と腕振り、フィニッシュ局面の力みのなさに注目してください。ストライド長とピッチのバランス、接地時間の短さも評価してください。`,
      }
    case '200m':
      return {
        role: '元日本代表の200m短距離コーチ',
        focus: `200mの選手です。カーブ走のリーン（内傾）、直線移行時の姿勢切り替え、後半100mでの速度維持に注目してください。腕振りの方向性（カーブでは体幹方向）、内側の肩の落ちすぎ、足首の硬さを評価してください。`,
      }
    case '400m':
      return {
        role: '元日本代表の400m短距離コーチ',
        focus: `400mの選手です。前半200mのコントロールされたスピード、カーブのリーン、300m地点からの乳酸への抵抗フォーム（体幹崩れ・腕振り乱れ）に特に注目してください。エネルギー配分が見えるフォームの変化を評価してください。`,
      }
    case '110mH':
    case '100mH':
      return {
        role: '元日本代表のハードルコーチ',
        focus: `${event}の選手です。ハードリング技術に最注目してください。リード脚の鋭い引き上げ・前傾、トレイル脚の素早い引き付け（外側への開きすぎ注意）、インターバルでの3歩リズム、ハードルへの接近角度・踏切距離を評価してください。`,
      }
    case '400mH':
      return {
        role: '元日本代表の400mハードルコーチ',
        focus: `400mHの選手です。13〜15歩のインターバルリズム、利き足・逆足でのハードリング対応、カーブハードルでのリーンと技術の両立、後半でのインターバル調整（歩数変更時の対応）を評価してください。`,
      }

    // ── 中距離 ────────────────────────────────────────────
    case '800m':
      return {
        role: '元日本代表の800m中距離コーチ',
        focus: `800mの選手です。スタートから200mの位置取りのためのポジショニング走、中間走の省エネフォーム（腕振り・接地・骨盤安定）、ラスト200mのスパート時のフォーム維持能力を評価してください。`,
      }
    case '1500m':
      return {
        role: '元日本代表の1500m中距離コーチ',
        focus: `1500mの選手です。集団走中の省エネフォーム、ミッドフット接地の有無、骨盤の安定性、肩の無駄な力み、ラスト1周のスパート時のフォーム変化に注目してください。`,
      }

    // ── 長距離 ────────────────────────────────────────────
    case '3000m':
    case '5000m':
    case '10000m':
      return {
        role: '元日本代表の長距離コーチ',
        focus: `${event}の選手です。経済的なランニングフォームが最重要です。接地パターン（ヒールvsミッドフット）、骨盤の前傾と安定性、肩・腕の無駄な力み、ピッチと歩幅のバランス、前傾姿勢の角度を評価してください。怪我につながるフォームの崩れも指摘してください。`,
      }
    case '3000mSC':
      return {
        role: '元日本代表の障害競走コーチ',
        focus: `3000mSCの選手です。障害越え技術（踏切タイミング・リード脚の高さ・バリア上での着地）、水濠（ウォーターハードル）の処理技術、そして通常走時の省エネフォームを評価してください。`,
      }
    case 'half_marathon':
    case 'marathon':
      return {
        role: '元日本代表のマラソンコーチ',
        focus: `${event === 'marathon' ? 'マラソン' : 'ハーフマラソン'}の選手です。超長距離の省エネフォームが最重要です。接地衝撃の最小化（オーバーストライドに注意）、腕振りの効率性、骨盤ドロップの有無、体幹の縦揺れ・横揺れを評価してください。後半の疲労によるフォーム崩れのリスクも分析してください。`,
      }

    // ── 走り幅跳び / 三段跳び ─────────────────────────────
    case '走幅跳':
      return {
        role: '元日本代表の走幅跳コーチ',
        focus: `走幅跳の選手です。助走後半のスピード維持、踏切前ペネルティステップ（重心沈み込み）、踏切での力の方向（前上方）と踏切脚の伸展速度、空中姿勢（反り跳び or はさみ跳び）、着地時のかかとからの前傾を評価してください。`,
      }
    case '三段跳':
      return {
        role: '元日本代表の三段跳コーチ',
        focus: `三段跳の選手です。ホップ・ステップ・ジャンプの比率バランス（理想は約35:30:35）、各局面での接地時間の短さ、ホップ着地からステップへの移行スムーズさ、最終跳躍での空中姿勢を評価してください。`,
      }
    case '走高跳':
      return {
        role: '元日本代表の走高跳コーチ',
        focus: `走高跳の選手です。助走のカーブ走でのリーン、踏切前の姿勢（上体起こし）、振り上げ脚・腕の爆発的引き上げ、バーを越える際の背面跳びフォーム（腰のアーチ、脚のウェービング）、着地の安全性を評価してください。`,
      }
    case '棒高跳':
      return {
        role: '元日本代表の棒高跳コーチ',
        focus: `棒高跳の選手です。助走スピードの維持、ポールの持ち方（グリップ幅・高さ）、ポールの植え付けタイミング、ロールバック〜反転の連動、バーを越える際の体の使い方（押しつけ〜振り切り）を評価してください。`,
      }

    // ── 投擲 ─────────────────────────────────────────────
    case '砲丸投':
      return {
        role: '元日本代表の砲丸投コーチ',
        focus: `砲丸投の選手です。グライド技術またはロータリー（回転）技術、体重移動のタイミング、下半身から体幹への力の連動、プット動作時の押し出しの方向性、リリース時の手首・指先のスナップを評価してください。サークル内の足位置・バランスも確認してください。`,
      }
    case 'やり投':
      return {
        role: '元日本代表のやり投コーチ',
        focus: `やり投の選手です。助走リズムとクロスステップ（5歩のリズム）、投擲腕の引き（肘の高さ・後方への引き）、体幹の回転と腕への力の伝達、リリースポイントの高さと最適投射角（35〜40°）、フォロースルーを評価してください。`,
      }
    case '円盤投':
      return {
        role: '元日本代表の円盤投コーチ',
        focus: `円盤投の選手です。バックスウィングの幅、1.5〜1.75回転の回転技術、「低く→高く」の重心変化、右踏切〜左脚ブロックの連動（右利きの場合）、リリース時の指先のかかりと腕の伸展角度を評価してください。`,
      }
    case 'ハンマー投':
      return {
        role: '元日本代表のハンマー投コーチ',
        focus: `ハンマー投の選手です。2回のスウィングでのボール軌道（低い軌道が理想）、3〜4回転の回転技術、低い重心と膝の曲げ込み、最大張力を引き出すための「引き」のタイミング、リリース方向と足位置の関係を評価してください。`,
      }

    default:
      return {
        role: '元日本代表の陸上競技コーチ',
        focus: `陸上競技（${event}）の選手です。全体的なフォームの効率性、体の各パーツの使い方、力の伝達、無駄な動きの有無を評価してください。`,
      }
  }
}

// ── プロンプト本体 ────────────────────────────────────────

export function getVideoAnalysisPrompt(event: AthleticsEvent): string {
  const { role, focus } = getContext(event)

  return `あなたは${role}です。
${focus}

提供された動画フレームを分析し、以下のJSONのみを返してください（前後のテキスト不要）：

{
  "technique_score": 0から100の整数,
  "feedback": "フォームの総評（選手への語りかけ形式で2〜3文、具体的に）",
  "strengths": ["良い点1（具体的に）", "良い点2"],
  "improvements": [
    "最優先改善点（なぜ重要か含めて）",
    "改善点2",
    "改善点3（任意）"
  ],
  "drills": [
    {
      "name": "ドリル名",
      "description": "やり方の説明（1〜2文）",
      "sets": "3×30m など具体的な量",
      "focus": "このドリルで改善される点"
    }
  ],
  "next_events": ["取り組むべき種目1", "種目2"],
  "event_detected": "動画から判断した種目（不明なら null）"
}`
}
